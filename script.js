// Function to display the category options pop-up box
function showCategoryOptions(categoryID) {
    var optionsPopup = document.getElementById(categoryID + "-options");
    optionsPopup.style.display = "block";
  }
  
  // Function to close the category options pop-up box
  function closeCategoryOptions(categoryID) {
    var optionsPopup = document.getElementById(categoryID + "-options");
    optionsPopup.style.display = "none";
  }
  
  